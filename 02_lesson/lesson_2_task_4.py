def fizz_buzz(n):
    """Печатает числа от 1 до n с заменой по правилам FizzBuzz.
    
    Правила:
    - Если число делится на 3 и 5, печатает 'FizzBuzz'
    - Если число делится только на 3, печатает 'Fizz'
    - Если число делится только на 5, печатает 'Buzz'
    - Иначе печатает само число
    
    Args:
        n (int): Верхняя граница диапазона чисел
    """
    for i in range(1, n + 1):
        if i % 15 == 0:
            print("FizzBuzz")
        elif i % 3 == 0:
            print("Fizz")
        elif i % 5 == 0:
            print("Buzz")
        else:
            print(i)


# Пример вызова функции
fizz_buzz(20)